
# String functions are built-in functions in Python that allow you to manipulate and work with strings. 
# Here are some examples

name = "puneet"
print(len(name))  # Output: 6
print(name.upper())  # Output: PUNEET
print(name.lower())  # Output: puneet
print(name.capitalize())  # Output: Puneet  
print(name.startswith("p"))   # Output: True
print(name.endswith("t"))   # Output: True  
print(name.replace("ee","i"))  # Output: punit  