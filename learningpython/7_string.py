"""string can be written in single quotes, double quotes, or triple quotes. 
Triple quotes are used for multi-line strings."""
name = "abcdefghijklmnopqrstuvwxyz"
print(name.upper())  # Output: ABCDEFGHIJKLMNOPQRSTUVWXYZ
print(name.lower())  # Output: abcdefghijklmnopqrstuvwxyz
print(len(name))  # Output: 26
print(name[0:26])  # Output: abcdefghijklmnopqrstuvwxyz
print(name[:26])  # Output: abcdefghijklmnopqrstuvwxyz
print(name[0:26])  # Output: abcdefghijklmnopqrstuvwxyz
print(name[0:26:3])  # Output: adgjmpsvy
print(name[::3])  # Output: adgjmpsvy
print(name[10])  # Output: k
a = "puneet sharma"
print(len(a))  # Output: 13 
